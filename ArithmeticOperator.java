public class ArithmeticOperator{
	public static void main(String[] args) {
		double num1=14.5, num2=2.5, result;
		result= num1+num2;
		System.out.println("num1+num2="+result);
		result= num1-num2;
		System.out.println("num1-num2="+result);
		result=num1*num2;
		System.out.println("num1*num2="+result);
		result=num1/num2;
		System.out.println("num1/num2="+result);
		result=num1%num2;
		System.out.println("num1/num2="+result);
		
	}
}
